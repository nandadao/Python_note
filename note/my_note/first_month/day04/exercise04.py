# 练习2：创建字符串：爱生活，我爱编程
message = "爱生活，我爱编程."
#     1.打印第一个字符，打印最后一个字符
print(message[0])
print(message[-1])
#     2.打印字符串长度，打印中间的字符
print(len(message))
print(message[len(message)//2])
#     3.打印前三个字符，后三个字符
print(message[:3])
print(message[-3:])
#     4.倒序打印所有字符
print(message[::-1])








