"""
编写一个程序，向一个文件中不断写入如下内容：

  1. 2019-1-1  18:18:18
  2. 2019-1-1  18:18:20
  3. 2019-1-1  18:18:22
  4. 2019-1-1  18:18:24
  5. 2019-1-1  18:19:56

  * 每次写入占一行
  * 这是一个死循环，2s写入一次，但是可以实时查看写入内容
  * 当程序退出，重新启动后，书写内容能够衔接之前
  序号内容
"""
from time import sleep,ctime

f = open('file','a+')

f.seek(0) # 文件偏移量定位到开头
n = 1
for i in f:
    n += 1   # 确定n的初始值

while True:
    sleep(2)
    msg = "%d. %s\n"%(n,ctime())
    f.write(msg)
    f.flush() # 刷新缓冲
    n += 1








